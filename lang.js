// Detect browser language
function detectBrowserLanguage() {
	const browserLang = navigator.language || navigator.userLanguage || 'en';
	const langCode = browserLang.split('-')[0].toLowerCase();
	// Check if we support this language
	if (LANGUAGES[langCode]) {
		return langCode;
	}
	return 'en';
}

// Current language (from localStorage or browser detection)
let currentLang = localStorage.getItem('lang') || detectBrowserLanguage();

// Get translation
function t(key, replacements = {}) {
	let text = LANGUAGES[currentLang]?.[key] || LANGUAGES['en'][key] || key;
	for (const [k, v] of Object.entries(replacements)) {
		text = text.replace(`{${k}}`, v);
	}
	return text;
}

// Set language
function setLanguage(lang) {
	if (LANGUAGES[lang]) {
		currentLang = lang;
		localStorage.setItem('lang', lang);
		return true;
	}
	return false;
}

// Get current language
function getLanguage() {
	return currentLang;
}

// Get all available languages
function getAvailableLanguages() {
	return Object.entries(LANGUAGES).map(([code, data]) => ({
		code,
		name: data.name,
		flag: data.flag
	}));
}
